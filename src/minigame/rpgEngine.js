export const MAP_ROWS = WORLD_MASK.length;
export const MAP_COLS = WORLD_MASK[0].length;
export const WORLD_WIDTH = MAP_COLS * TILE_SIZE;
export const WORLD_HEIGHT = MAP_ROWS * TILE_SIZE;

// Một ô đặc (tường) hay trống (đi được)?
const isCellBlocked = (col, row) => {
  if (col < 0 || row < 0 || col >= MAP_COLS || row >= MAP_ROWS) return true;
  return WORLD_MASK[row][col] === "#";
};

// (cx, cy) là TÂM vật thể, radius là bán kính. true nếu chạm tường hoặc ra ngoài bản đồ.
export const checkMapCollisions = (cx, cy, radius = 16) => {
  if (cx - radius < 0 || cy - radius < 0 || cx + radius > WORLD_WIDTH || cy + radius > WORLD_HEIGHT) {
    return true;
  }
  const col0 = Math.floor((cx - radius) / TILE_SIZE);
  const col1 = Math.floor((cx + radius) / TILE_SIZE);
  const row0 = Math.floor((cy - radius) / TILE_SIZE);
  const row1 = Math.floor((cy + radius) / TILE_SIZE);
  for (let row = row0; row <= row1; row++) {
    for (let col = col0; col <= col1; col++) {
      if (isCellBlocked(col, row)) return true;
    }
  }
  return false;
};

export const isWalkable = (cx, cy, radius = 16) => !checkMapCollisions(cx, cy, radius);
